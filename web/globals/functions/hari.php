<?php

function hari($a) {
	if($a=='1')
	{
		$hari='Senin';
	}
	elseif($a=='2')
	{
		$hari='Selasa';
	}
	elseif($a=='3')
	{
		$hari='Rabu';
	}
	elseif($a=='4')
	{
		$hari='Kamis';
	}
	elseif($a=='5')
	{
		$hari='Jumat';
	}
	elseif($a=='6')
	{
		$hari='Sabtu';
	}
	elseif($a=='7')
	{
		$hari='Minggu';
	}
	return $hari;
}


